Abstract Class(1)
-Sebuah kelas yang tidak dapat di-inisiasi.
-Kelas'abstrak'.
-Mendefinisikan interface untuk kelas lain yang menjadi turunannya.
-Berperan sebagai 'kerangka dasar' untuk kelas turunannya.
-Memiliki minimal 1 method abstrak.
-Digunakan dalam 'pwarisan'/inheritance untuk 'memaksakan' implementasi method abstrak yang sama untuk semua kelas turunannya.

Abstract Class(2)
-Semua kelas turunan, harus mengimplementasikan method abstrak yang ada di kelas abstraknya.
-Kelas abstrak boleh memiliki property / method reguler.
-Kelas abstrak boleh memiliki property / static method.

Kenapa menggunakan kelas abstrak ?
-Mempresentasikan ide atau konsep dasar, yang nantinya akan di implementasikan di kelas" turunannya.
-Composition Over Inheritance, Jadi sebaiknya kita melakukan komposisi dibanding melakukan inheritance/pewarisan begitu saja.
-Salah satu cara menerapkan Polimorphism.
-Sentralisasi logic.
-Mempermudah pengerjaan tim.

